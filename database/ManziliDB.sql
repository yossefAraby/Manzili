
CREATE TABLE PERSON
(
  PersonID INT NOT NULL Identity(1,1),
  Email VARCHAR(100) NOT NULL UNIQUE,
  Password VARCHAR(50) NOT NULL, 
  Created_at DATETIME NULL DEFAULT GETDATE(),
  First_Name NVARCHAR(50) NULL, 
  Last_Name NVARCHAR(50) NULL,
  PRIMARY KEY (PersonID)
);

CREATE TABLE PERSON_PERMISSIONS
(
  Permission INT NOT NULL,
  PersonID INT NOT NULL,
  PRIMARY KEY (Permission, PersonID),
  FOREIGN KEY (PersonID) REFERENCES PERSON(PersonID) ON DELETE CASCADE
);

CREATE TABLE ADDRESSES
(
  ID INT NOT NULL Identity(1,1),
  City NVARCHAR(50) NULL,
  Street NVARCHAR(100) NULL,
  BuildingNumber VARCHAR(20) NULL,
  ApartmentNumber VARCHAR(20) NULL,
  ZipCode VARCHAR(20) NULL,
  PersonID INT NULL,
  PRIMARY KEY (ID),
  FOREIGN KEY (PersonID) REFERENCES PERSON(PersonID) ON DELETE SET NULL
);

CREATE TABLE ADDRESSES_AND_CONTACT_PHONENUMBER
(
  PhoneNumber VARCHAR(20) NOT NULL,
  AddressID INT NOT NULL,
  PRIMARY KEY (PhoneNumber, AddressID),
  FOREIGN KEY (AddressID) REFERENCES ADDRESSES(ID) ON DELETE CASCADE
);

CREATE TABLE WISHLIST
(
  WichListID INT NOT NULL Identity(1,1),
  NumberOfProducts INT NULL Default 0,
  PRIMARY KEY (WichListID)
);

CREATE TABLE ENDUSER
(
  EndUserID INT NOT NULL ,
  WichListID INT NULL,
  PersonID INT NOT NULL Unique, 
  PRIMARY KEY (EndUserID),
  FOREIGN KEY (WichListID) REFERENCES WISHLIST(WichListID) ON DELETE SET NULL,
  FOREIGN KEY (PersonID) REFERENCES PERSON(PersonID) ON DELETE CASCADE
);

CREATE TABLE SELLER
(
  SellerID INT NOT NULL ,
  StoreName NVARCHAR(100) NULL,
  Store_Description NVARCHAR(max) NULL,
  StoreLogo VARBINARY(max) NULL,  
  SellerWallet_Balance DECIMAL(12,2) NULL DEFAULT 0.00,
  PersonID INT NOT NULL Unique, 
  PRIMARY KEY (SellerID),
  FOREIGN KEY (PersonID) REFERENCES PERSON(PersonID) ON DELETE CASCADE
);

CREATE TABLE FOLLOW
(
  Followed_at DATE NULL DEFAULT GetDate(),
  EndUserID INT NOT NULL,
  SellerID INT NOT NULL,
  PRIMARY KEY (EndUserID, SellerID),
  FOREIGN KEY (EndUserID) REFERENCES ENDUSER(EndUserID),
  FOREIGN KEY (SellerID) REFERENCES SELLER(SellerID) ON DELETE CASCADE
);

CREATE TABLE SHIPMENT
(
  ShipmentID INT NOT NULL IDENTITY(1,1),
  Carrier NVARCHAR(100) NULL,
  Shipment_Status TINYINT NULL,
  Shipped_at_time DATETIME NULL,
  Delivered_at_time DATE NULL,
  Tracking_Number VARCHAR(50) NULL,
  PRIMARY KEY (ShipmentID)
);

CREATE TABLE CATEGORY
(
  CategoryID TINYINT NOT NULL Identity(1,1),
  Category_Name NVARCHAR(25) NOT NULL,
  PRIMARY KEY (CategoryID)
);

CREATE TABLE PAYOUT
(
  PayoutID INT NOT NULL IDENTITY(1,1),
  Amount DECIMAL(12,2) NULL,
  Status INT NULL,
  Request_at_time DATETIME NULL,
  Paid_at DATETIME NULL Default GetDate(),
  SellerID INT NULL,
  PRIMARY KEY (PayoutID),
  FOREIGN KEY (SellerID) REFERENCES SELLER(SellerID) ON DELETE SET NULL
);

CREATE TABLE [Orders]
(
  OrderID INT NOT NULL IDENTITY(1,1),
  Status TINYINT NULL,
  ShippingCost DECIMAL(10,2) NULL,
  TotalAmount DECIMAL(12,2) NULL,
  PaymentID INT NOT NULL,
  PaymentMethod TINYINT NULL,	-- <----------- Important
  PSP VARCHAR(50) NULL,	-- <----------- Important
  PaymentStatus TINYINT NULL,
  Transaction_REF Varchar(50) NULL,
  Paid_at DATETIME NOT NULL,
  ShipmentID INT NULL,
  SellerID INT NULL,
  EndUserID INT NULL,
  PRIMARY KEY (OrderID),
  FOREIGN KEY (ShipmentID) REFERENCES SHIPMENT(ShipmentID) ON DELETE SET NULL,
  FOREIGN KEY (SellerID) REFERENCES SELLER(SellerID) ON DELETE NO ACTION,
  FOREIGN KEY (EndUserID) REFERENCES ENDUSER(EndUserID) ON DELETE NO ACTION
);

CREATE TABLE STANDARD_ORDER
(
  --StandardOrderID INT NOT NULL Identity(1,1),
