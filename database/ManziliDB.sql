CREATE TABLE COUPONS
(
  CouponID INT NOT NULL IDENTITY(1,1),
  Description NVARCHAR(500) NOT NULL,
  Created_at DATETIME NOT NULL DEFAULT GetDate(),
  Status BIT NULL DEFAULT 1,
  CouponCode VarChar(50) Unique,
  Expired_Date DATETIME NULL,
  Scope TINYINT NOT NULL,
  CreatorID INT NOT NULL,
  StoreID INT NULL,
  PRIMARY KEY (CouponID),
  CONSTRAINT Check_Coupon_Scope CHECK(Scope IN (0,10,11,21,20)),
  FOREIGN KEY (CreatorID) REFERENCES PERSON(PersonID) ON DELETE NO ACTION,
  FOREIGN KEY (StoreID) REFERENCES Seller(SellerID) ON DELETE NO ACTION

);

CREATE TABLE Coupon_Product
(
  Coupon_ProductID INT IDENTITY(1,1),
  CouponID INT NOT NULL,
  ProductID INT NOT NULL,
  PRIMARY KEY (Coupon_ProductID),
  FOREIGN KEY (CouponID) REFERENCES COUPONS(CouponID) ON DELETE CASCADE,
  FOREIGN KEY (ProductID) REFERENCES [PRODUCTS](ProductID) ON DELETE CASCADE
);

CREATE TABLE USER_COUPONS
(
  Used_CouponID INT NOT NULL IDENTITY(1,1),
  Used_at DATETIME NOT NULL DEFAULT GETDATE(), 
  CouponID INT NOT NULL,
  UserID INT NOT NULL,
  OrderID INT NOT NULL,
  PRIMARY KEY (Used_CouponID),
  FOREIGN KEY (CouponID) REFERENCES COUPONS(CouponID),
  FOREIGN KEY (UserID) REFERENCES PERSON(PersonID),
  FOREIGN KEY (OrderID) REFERENCES Orders(OrderID) ON DELETE CASCADE
);



