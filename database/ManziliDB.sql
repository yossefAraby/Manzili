CREATE TABLE [PRODUCTS]
(
  ProductID INT NOT NULL Identity(1,1),
  ProductName NVARCHAR(50) NOT NULL, 
  Cover VARBINARY(max) NULL,        
  CategoryID TINYINT NULL,
  SellerID INT NULL,
  PRIMARY KEY (ProductID),
  FOREIGN KEY (CategoryID) REFERENCES CATEGORY(CategoryID) ON DELETE SET NULL,
  FOREIGN KEY (SellerID) REFERENCES SELLER(SellerID) ON DELETE CASCADE
);

CREATE TABLE PRODUCT_IMAGES
(
  Product_ImageID INT NOT NULL Identity(1,1),
  ProductImage VARBINARY(max) NOT NULL,
  Upload_date DATE NOT NULL DEFAULT GetDate(),
  ProductID INT NOT NULL,
  PRIMARY KEY (Product_ImageID, ProductID),
  FOREIGN KEY (ProductID) REFERENCES [PRODUCTS](ProductID) ON DELETE CASCADE
);

CREATE TABLE REVIEWING_AND_RATING
(
  Rating FLOAT NOT NULL,
  Comment NVARCHAR(500) NULL,
  Created_at DATETIME NOT NULL DEFAULT GETDATE(),
  EndUserID INT NOT NULL,
  ProductID INT NOT NULL,
  PRIMARY KEY (EndUserID, ProductID),
  FOREIGN KEY (EndUserID) REFERENCES ENDUSER(EndUserID),
  FOREIGN KEY (ProductID) REFERENCES [PRODUCTS](ProductID) ON DELETE CASCADE
);

CREATE TABLE CONSIST_OF
(
  Quantity INT NOT NULL,
  Price_at_purchase DECIMAL(12,2) NOT NULL,
  OrderID INT NOT NULL,
  ProductID INT NOT NULL,
  PRIMARY KEY (OrderID, ProductID),
  FOREIGN KEY (OrderID) REFERENCES Orders(OrderID) ON DELETE CASCADE,
  FOREIGN KEY (ProductID) REFERENCES [PRODUCTS](ProductID)
);

CREATE TABLE Whishlist_Contain
(
  WichListID INT NOT NULL,
